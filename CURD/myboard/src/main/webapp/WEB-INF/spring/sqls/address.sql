CREATE TABLE ADDR(
	ADDRNO NUMBER PRIMARY KEY,
	ADDNAME VARCHAR2(100),
	USERNAME VARCHAR2(1000),
	ADDRESS_1 VARCHAR2(1000),
	ADDRESS_2 VARCHAR2(1000),
	ADDNO VARCHAR2(100),
	MAINADDR CHAR
);

